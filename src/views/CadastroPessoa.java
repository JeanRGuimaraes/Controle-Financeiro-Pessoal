 private boolean verificaDados() {
        String erro = "";
        
        //if (!codigo.getText().matches("^[0-9]*$") || txtValor.getText().isEmpty()) {
          //  erro += "\n- Campo de Codigo";
        //}
        
        //if (!txtValor.getText().replace(".", "").matches("^[0-9]*$") || txtValor.getText().isEmpty()) {
        //    erro += "\n- Campo de Valor";
        //}

        if (nome.getText().equals("")) {
            erro += "\n- Campo de Descri√ß√£o";
        }

        if (profisao.getText().equals("  /  /    ")) {
            erro += "\n- Campo do Periodo Inicial";
        }
        
        if (email.getText().equals("  /  /    ")) {
            erro += "\n- Campo do Periodo Final";
        }
        if (erro.length() > 1) {
            JOptionPane.showMessageDialog(null, "Os seguintes Campos est√£o invalidos" + erro, "CFP - Informa", JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }
    
