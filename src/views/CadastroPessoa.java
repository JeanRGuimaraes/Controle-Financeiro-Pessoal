    public clPessoa getiCodPessoa(){
        //alimentar o objeto com informacoes do textField
        clPessoa pessoa = new clPessoa();
        if(verificaDados())
        {
            pessoa.setiCodPessoa(Integer.parseInt(codigo.getText()));
            pessoa.setiCpf(Integer.parseInt(cpf.getText()));
            pessoa.setiRg(Integer.parseInt(rg.getText()));
            pessoa.setiTelefone(Integer.parseInt(telefone.getText()));
            pessoa.setStrNome(nome.getText());
            //pessoa.setStrGenero(genero.getText());
            pessoa.setStrEmail(email.getText());
            pessoa.setStrProfissao(profisao.getText());
        }
      
         return new clPessoa();
    }
    
     public clPessoa limparCampos()
    {
        clPessoa pessoa = new clPessoa();
        
            codigo.setText("");
            nome.setText("");
            cpf.setText("");
            rg.setText("");
            telefone.setText("");
            profisao.setText("");
            email.setText("");
            
        return pessoa;
    }
    
